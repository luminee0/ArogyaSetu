-- Create Doctors Table
CREATE TABLE public.doctors (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  specialty TEXT,
  role TEXT,
  experience TEXT,
  location TEXT,
  fee TEXT,
  satisfaction TEXT,
  stories TEXT,
  avatar TEXT,
  availability TEXT[], 
  gender TEXT
);

